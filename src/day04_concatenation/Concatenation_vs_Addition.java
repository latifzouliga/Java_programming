package day04_concatenation;

public class Concatenation_vs_Addition {

    public static void main(String[] args) {

        System.out.println("32" + 1); // this is concatenation
        System.out.println(7 + "1"); // "71"
        System.out.println("13"+"2"); // "132"
        System.out.println("------------------------------------");

        System.out.println(12 + 2.5); // this is addition because there is no string
        System.out.println("-------------------------------------");

        System.out.println("The value is "+ true); // this is concatenation



    }



}
