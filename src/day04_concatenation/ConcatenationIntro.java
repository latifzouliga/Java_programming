package day04_concatenation;

public class ConcatenationIntro {

    public static void main(String[] args) {

        String name = "Alexander";
        int age = 35;
        int salary = 85_000; // the underscore in the number in to make it more readable

        System.out.println("Hello " + name + ".");
        System.out.println("You are " + age + "years old");
        System.out.println("Your salary is " + "$" + salary);
// there has to be a string (before or after + operator) in order to be a CONCATENATION. If there is no string it will be an addition











    }


}
